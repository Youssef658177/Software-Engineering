@R2
M=0      // تصفير الناتج في البداية: R2 = 0

(LOOP)
  @R1
  D=M    // نقل قيمة العداد (R1) إلى المسجل D لفحصها
  
  @END
  D;JEQ  // شرط الخروج: إذا كانت قيمة R1 تساوي 0، اقفز إلى النهاية (END)

  @R0
  D=M    // جلب قيمة المضروب (R0) ووضعها في المسجل D
  
  @R2
  M=D+M  // إضافة قيمة R0 إلى الناتج الحالي: R2 = R2 + R0

  @R1
  M=M-1  // إنقاص العداد بمقدار 1: R1 = R1 - 1

  @LOOP
  0;JMP  // قفز غير مشروط: ارجع إلى بداية الحلقة (LOOP) لتكرار العملية

(END)
  @END
  0;JMP  // حلقة لانهائية لإيقاف المعالج ومنعه من قراءة تعليمات عشوائية بعد انتهاء البرنامج
